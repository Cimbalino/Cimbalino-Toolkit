namespace Cimbalino.Toolkit.Services
{
    /// <summary>
    /// Describes the scale factor of the immersive environment.
    /// </summary>
    public enum DisplayPropertiesServiceResolutionScale
    {
        /// <summary>
        /// Specifies the scale of a display is invalid.
        /// </summary>
        Invalid = 0,

        /// <summary>
        /// Specifies the scale of a display as 100 percent.
        /// </summary>
        Scale100Percent = 100,

        /// <summary>
        /// Specifies the scale of a display as 120 percent.
        /// </summary>
        Scale120Percent = 120,

        /// <summary>
        /// Specifies the scale of a display as 140 percent.
        /// </summary>
        Scale140Percent = 140,

        /// <summary>
        /// Specifies the scale of a display as 150 percent.
        /// </summary>
        Scale150Percent = 150,

        /// <summary>
        /// Specifies the scale of a display as 160 percent.
        /// </summary>
        Scale160Percent = 160,

        /// <summary>
        /// Specifies the scale of a display as 180 percent.
        /// </summary>
        Scale180Percent = 180,

        /// <summary>
        /// Specifies the scale of a display as 225 percent.
        /// </summary>
        Scale225Percent = 225,
    }
}